import SwiftUI
import CoreML

struct ContentView: View {
    @StateObject private var viewModel = PredictionViewModel()
    
    var body: some View {
        NavigationView {
            VStack {
                Text("Predicción de Precio de Apartamento")
                    .font(.title2)
                    .bold()
                    .padding(.bottom, 10)
                
                Group {
                    TextField("Área (m²)", text: $viewModel.area)
                        .keyboardType(.decimalPad)
                    TextField("Habitaciones", text: $viewModel.habitaciones)
                        .keyboardType(.numberPad)
                    TextField("Baños", text: $viewModel.banos)
                        .keyboardType(.numberPad)
                    TextField("Administración (COP)", text: $viewModel.administracion)
                        .keyboardType(.decimalPad)
                    TextField("Parqueaderos", text: $viewModel.parqueaderos)
                        .keyboardType(.numberPad)
                    HStack {
                        Text("Estrato:")
                        Picker("Estrato", selection: $viewModel.estrato) {
                            ForEach(1...6, id: \.self) { value in
                                Text("\(value)").tag("\(value)")
                            }
                        }
                        .pickerStyle(MenuPickerStyle())
                    }
                    
                    TextField("Antiguedad (años)", text: $viewModel.antiguedad)
                        .keyboardType(.numberPad)
                }
                .textFieldStyle(RoundedBorderTextFieldStyle())
                .padding(.horizontal)
                
                Button(action: {
                    viewModel.predecirPrecio()
                }) {
                    Text("Predecir Precio")
                        .bold()
                        .frame(maxWidth: .infinity)
                        .padding()
                        .background(Color.blue)
                        .foregroundColor(.white)
                        .cornerRadius(10)
                }
                .padding(.horizontal)
                .padding(.top)
                
                if let precio = viewModel.precioPredicho {
                   Text("Precio Predicho: \(precio, format: .currency(code: "COP"))")
                       .font(.headline)
                       .foregroundColor(.green)
                       .padding(.top)
               }
                if let error = viewModel.mensajeError {
                    Text(error)
                        .foregroundColor(.red)
                        .padding(.top)
                }
                
                Spacer()
            }
            .padding()
        }
    }
}


